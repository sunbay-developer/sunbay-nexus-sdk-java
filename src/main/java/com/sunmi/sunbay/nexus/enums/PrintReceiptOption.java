package com.sunmi.sunbay.nexus.enums;

/**
 * Print receipt option enum
 *
 * @since 2026-01-28
 */
public enum PrintReceiptOption {

    /**
     * Do not print receipt
     */
    NONE,

    /**
     * Print merchant copy only
     */
    MERCHANT,

    /**
     * Print customer copy only
     */
    CUSTOMER,

    /**
     * Print both merchant and customer copies
     */
    BOTH
}
